public class L22_do_WhileLoop {
    public static void main(String[] args) {
        // While LOOP --> (first Check then proceed)
        // int i = 10;
        // while(i<5){
        //     System.out.println(i);
        //     i++;
        // }


        // Do-While LOOP --> (First run then check, thus it run atleast Once)
        int j = 20;
        do{
            System.out.println(j);
            j++;
        }while(j<5);
    }
}
